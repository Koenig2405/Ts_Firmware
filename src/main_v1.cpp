void displaySetup()
{
    if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
    { /*I2C Address at which OLED will communicate*/
        Serial.println(F("SSD1306 allocation failed"));
        for (;;)
            ;
    }

    display.clearDisplay();                 /*Clear display*/
    display.setTextSize(2);                 /*OLED screen text size defined*/
    display.setTextColor(WHITE);            /*OLED screen text color*/
}

void setup() 
{
    Serial.begin(9600);
    displaySetup();

    pinMode(EncSW, INPUT);
    attachInterrupt(digitalPinToInterrupt(EncSW), enterInterrupt, FALLING);

    pinMode(buzzer, OUTPUT);
    digitalWrite(buzzer, true);
    
    Timer1.initialize(1000000/PWM_FREQUENZ);	// Initialisierung des PWM Timers
	Timer1.pwm(LUEFTER_PIN,0);			// PWM des Timers setzen, zun√§chst 0
	pinMode(LUEFTER_PIN,OUTPUT);
    Timer1.setPwmDuty(LUEFTER_PIN, map(0,0,100,0,1023));

}

void loop() 
{
    switch (prg_cnt)
    {
    case 0:
        showBoot();
        prg_cnt = 1;
        break;

    case 1:
        globalFanspeed = selectFanSpeed();
        enterState = false;
        prg_cnt = 2;
        break;

    case 2:
        globalDuration = selectDuration();
        enterState = false;
        prg_cnt = 3;
        break;

    case 3:
        globalAlarm = setAlarm();
        enterState = false;
        prg_cnt = 4;
        break;

    case 4:
        fanFin = turnFanOn();
        enterState = false;
        prg_cnt = 1;


    
    default:
        break;
    }
}

void enterInterrupt()
{
    delay(150);
    if(digitalRead(EncSW) == false)
    {
        enterState = true;
        digitalWrite(buzzer, false);
        Serial.println("Interrupt:: Button pressed!");
        delay(500);
        digitalWrite(buzzer, true);
    }
    EIFR = (0b11 << INTF0);
}

